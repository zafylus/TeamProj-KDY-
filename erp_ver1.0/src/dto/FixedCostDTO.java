package dto;

public class FixedCostDTO {
	private String fi_name;
	private int fi_cost;
	private int fi_date;
	
	public String getFi_name() {
		return fi_name;
	}
	public void setFi_name(String fi_name) {
		this.fi_name = fi_name;
	}
	public int getFi_cost() {
		return fi_cost;
	}
	public void setFi_cost(int fi_cost) {
		this.fi_cost = fi_cost;
	}
	public int getFi_date() {
		return fi_date;
	}
	public void setFi_date(int fi_date) {
		this.fi_date = fi_date;
	}
	
	
}
